CREATE TABLE IF NOT EXISTS AUDITORIA_USUARIO (
  AUDIT_ID CHAR(36) NOT NULL PRIMARY KEY,
  USR_ID CHAR(36) NOT NULL,
  ACCION VARCHAR(10) NOT NULL, -- INSERT, UPDATE, DELETE
  CAMPO_MODIFICADO VARCHAR(30),
  VALOR_ANTIGUO VARCHAR(300),
  VALOR_NUEVO VARCHAR(300),
  FECHA_MODIFICACION DATETIME NOT NULL DEFAULT current_timestamp(),
  FOREIGN KEY (USR_ID) REFERENCES USUARIO(USR_ID)
);

CREATE TABLE IF NOT EXISTS AUDITORIA_USUARIO_PROFESOR (
  AUDIT_ID CHAR(36) NOT NULL PRIMARY KEY,
  DTLL_PRF_ID CHAR(36) NOT NULL,
  ACCION VARCHAR(10) NOT NULL, -- INSERT, UPDATE, DELETE
  CAMPO_MODIFICADO VARCHAR(30),
  VALOR_ANTIGUO VARCHAR(300),
  VALOR_NUEVO VARCHAR(300),
  FECHA_MODIFICACION DATETIME NOT NULL DEFAULT current_timestamp(),
  FOREIGN KEY (DTLL_PRF_ID) REFERENCES USUARIO_PROFESOR (DTLL_PRF_ID)
);
